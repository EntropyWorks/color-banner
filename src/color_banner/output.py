from __future__ import annotations

import base64
from pathlib import Path

_ATTRIBUTION_LINE1 = "Based on Calligraphy by GeopJr <https://codeberg.org/GeopJr/Calligraphy>"
_ATTRIBUTION_LINE2 = 'Originally by Gregor "gregorni" Niehl — Licensed under GPL v3'


def write_stdout(lines: list[str]) -> None:
    """Print lines to stdout joined by newlines."""
    print("\n".join(lines))


def _build_ansi_header(version: str, extra: str = "") -> str:
    """Build the standard ANSI file header with an optional extra comment line.

    Args:
        version: color-banner version string.
        extra: Optional extra comment line (e.g. ``"# Font: slant (#001)\\n"``).
               Must include a trailing newline if provided.
    """
    base = (
        f"# Generated by color-banner {version}\n"
        f"# {_ATTRIBUTION_LINE1}\n"
        f"# {_ATTRIBUTION_LINE2}\n"
    )
    if extra:
        base += extra
    return base + "# cat this file to display the banner\n"


def write_ansi_file(lines: list[str], path: str, version: str) -> None:
    """Write ANSI-escaped lines to a file with an attribution header.

    Parent directories are created automatically.
    The resulting file can be replayed with: cat <path>
    Raises OSError on write failure.
    """
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    content = _build_ansi_header(version) + "\n".join(lines) + "\n"
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(content)


def write_ansi_files_all(
    font_banners: list[tuple[int, str, list[str]]],
    dir_path: str,
    version: str,
) -> None:
    """Write one ANSI file per font into dir_path.

    Each file is named NNN-fontname.ans where NNN is the zero-padded
    1-based font number.  dir_path is created automatically if needed.

    Args:
        font_banners: list of (number, font_name, lines) tuples.
        dir_path: output directory path (created if it does not exist).
        version: color-banner version string for the file header.
    """
    out = Path(dir_path)
    out.mkdir(parents=True, exist_ok=True)
    for num, font_name, lines in font_banners:
        filename = f"{num:03d}-{font_name}.ans"
        file_path = out / filename
        header = _build_ansi_header(
            version, extra=f"# Font: {font_name} (#{num:03d})\n"
        )
        content = header + "\n".join(lines) + "\n"
        with open(file_path, "w", encoding="utf-8") as fh:
            fh.write(content)


def write_shell_export(
    lines: list[str],
    path: str,
    version: str,
    function_name: str = "show_banner",
) -> None:
    """Write a self-contained bash function that replays the banner.

    The ANSI output is base64-encoded and embedded inline.
    Raises OSError on write failure.
    """
    ansi_bytes = ("\n".join(lines) + "\n").encode("utf-8")
    b64 = base64.b64encode(ansi_bytes).decode("ascii")
    script = (
        f"#!/usr/bin/env bash\n"
        f"# Generated by color-banner {version}\n"
        f"# {_ATTRIBUTION_LINE1}\n"
        f"# {_ATTRIBUTION_LINE2}\n"
        f"# Paste this function into your script or .bashrc\n"
        f"{function_name}() {{\n"
        f"  printf '%s' \"$(printf '%s' '{b64}' | base64 -d)\"\n"
        f"}}\n"
        f"{function_name}\n"
    )
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(script)
